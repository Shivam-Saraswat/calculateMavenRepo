package com.cg.maven.demo.CalculatorApp;

public class Calculator {
	public int add(int a, int b) {
		return a + b;
	}

	public int substract(int a, int b) {
		return a - b;
	}

	public int multiply(int a, int b) {
		return a * b;
	}

	public double divison(int a, int b) {
		return (double) a / b;
	}
}
